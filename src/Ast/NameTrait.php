declare(strict_types=1);

namespace Phplrt\Ast;

/**
 * Trait NameTrait
 */
trait NameTrait
{
    /**
     * @var string
     */
    protected $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return bool
     */
    public function is(string $name): bool
    {
        return $this->name === $name;
    }
}
